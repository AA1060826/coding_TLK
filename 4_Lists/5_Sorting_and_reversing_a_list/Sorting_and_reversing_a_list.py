#Create initial List
fruits = ['banana', 'apple', 'grape', 'kiwi', 'orange']
#use sort command to sort in ascending order
fruits.sort()
#print
print(fruits)
#use 'reverse=True' to rever ascending order into descending order
fruits.sort(reverse=True)
#print
print(fruits)