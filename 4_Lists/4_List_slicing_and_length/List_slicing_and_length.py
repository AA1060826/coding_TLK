# Create a list of colors
colors = ['red', 'blue', 'green', 'yellow', 'purple']

# Slice the list to get the second and third colors
sliced_colors = colors[1:3]

# Print the sliced list
print(sliced_colors)

# Print the length of the colors list
print(len(colors))